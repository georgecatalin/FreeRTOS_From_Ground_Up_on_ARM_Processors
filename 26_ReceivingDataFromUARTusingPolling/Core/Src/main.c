#define STACK_SIZE 128  // 128 WORDS -> STM32 uses 4 Bytes for one word -> 128 x 4 = 512 bytes memory used
QueueHandle_t xQueue;

void pollingUART3Task(void *pvParams);
void handlingUART3Task(void *pvParams);


int main(void)
{
  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();


  gpio_init();

  xTaskCreate(pollingUART3Task,"pollingUART", STACK_SIZE, NULL, tskIDLE_PRIORITY +2, NULL);
  xTaskCreate(handlingUART3Task,"handlingUART", STACK_SIZE, NULL, tskIDLE_PRIORITY +3, NULL);

  xQueue = xQueueCreate(10, sizeof(char));

  vTaskStartScheduler();

  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  status_button = read_sensor_status();
	  sensor_value = read_adc_sensor_data();
  }
  /* USER CODE END 3 */
}

void pollingUART3Task(void *pvParams)
{
	uint8_t byteReceived;
	USART3_UART_RX_Init();

	while(1)
	{
		byteReceived = uart3_read();
		xQueueSend(xQueue, &byteReceived, 0);
	}
}

char bytes_received;
void handlingUART3Task(void *pvParams)
{
	while(1)
	{
		xQueueReceive(xQueue, &bytes_received, portMAX_DELAY);
	}
}



