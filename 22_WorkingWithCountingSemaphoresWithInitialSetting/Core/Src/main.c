void vDigital_Sensor_Task(void *pvParams)
{
	 gpio_init();
	 while(1)
	 {
		 status_button = read_sensor_status();
		 if(xSemaphoreTake(xSerialSemaphore, (TickType_t) 5) == pdTRUE)
		 {
			 printf("The status of the button is %d.\n\r", status_button);
			 xSemaphoreGive(xSerialSemaphore);
		 }
		 vTaskDelay(1);
	 }
}

void vAnalog_Sensor_Task(void *pvParams)
{
	adc2_init();
	while(1)
	{
		 sensor_value = read_adc_sensor_data();
		 if(xSemaphoreTake(xSerialSemaphore, (TickType_t) 5)== pdTRUE)
		 {
			 printf("The value read from sensor is %ld \n\r", sensor_value);
			 xSemaphoreGive(xSerialSemaphore);
		 }
		 vTaskDelay(1);
	}
}


